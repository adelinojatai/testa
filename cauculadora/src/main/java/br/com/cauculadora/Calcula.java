package br.com.cauculadora;

public class Calcula {


 public Double soma (Double valorA, Double valorB){
 	Double retorno = valorA+valorB;
 	return retorno; 
 }
 public Double subtral (Double valorA, Double valorB){
	 	Double retorno = valorA-valorB;
	 	return retorno; 
 }
 public Double multiplica (Double valorA, Double valorB){
	 	Double retorno = valorA*valorB;
	 	return retorno; 
 }
 public Double divide (Double valorA, Double valorB){
	 	Double retorno = valorA/valorB;
	 	return retorno; 
 }
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
 	
}